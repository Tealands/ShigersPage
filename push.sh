# Gitの変更をステージングしてコミットし、リモートリポジトリにプッシュするスクリプト
# 使用方法: ./git_push.sh "コミットメッセージ"

if [ $# -ne 1 ]; then
    echo "エラー: 引数が不正です"
    echo "使用方法: $0 \"コミットメッセージ\""
    exit 1
fi
# コミットメッセージ
commit_message="$1"
# 変更をステージング
git add .
# コミット
git commit -m "$commit_message"
# プッシュ
git push origin main

